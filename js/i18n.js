// Initialize translation system
let currentLang = localStorage.getItem("funjuLang") || "ua";

// Helper function to get translation
function t(key, params = {}) {
  let text = translations[currentLang][key] || key;

  // Replace {param} with actual values
  Object.keys(params).forEach(param => {
    text = text.replace(new RegExp(`\\{${param}\\}`, 'g'), params[param]);
  });

  return text;
}

// Make it globally available
window.t = t;
window.getCurrentLang = () => currentLang;

function switchLanguage(lang) {
  currentLang = lang;
  localStorage.setItem("funjuLang", lang);

  // Update HTML lang attribute
  document.documentElement.lang = lang === "ua" ? "uk" : "en";

  // Update page title
  document.title = translations[lang].pageTitle;

  // Update language switcher button to show opposite language
  const langFlag = document.getElementById("langFlag");

  if (langFlag) {
    if (lang === "ua") {
      // Currently UA, show US flag for EN option
      langFlag.textContent = "üá∫üá∏";
    } else {
      // Currently EN, show UA option
      langFlag.textContent = "üá∫üá¶";
    }
  }

  // Update all elements with data-i18n attribute
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.getAttribute("data-i18n");
    if (translations[lang][key]) {
      element.textContent = translations[lang][key];
    }
  });

  // Update all elements with data-i18n-placeholder attribute
  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    const key = element.getAttribute("data-i18n-placeholder");
    if (translations[lang][key]) {
      element.placeholder = translations[lang][key];
    }
  });

  // Update select options
  const citySelect = document.getElementById("userCity");
  if (citySelect) {
    citySelect.options[0].text = translations[lang].findSelectCity;
  }

  // Trigger custom event for other scripts to listen to
  window.dispatchEvent(new CustomEvent("languageChanged", { detail: { lang } }));
}

// Initialize on page load
document.addEventListener("DOMContentLoaded", () => {
  // Set up language switcher button (single toggle button)
  const langSwitcher = document.getElementById("langSwitcher");

  if (langSwitcher) {
    langSwitcher.addEventListener("click", () => {
      // Toggle between languages
      const newLang = currentLang === "ua" ? "en" : "ua";
      switchLanguage(newLang);
    });
  }

  // Apply saved language or default
  switchLanguage(currentLang);
});
